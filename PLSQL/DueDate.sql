DECLARE
    v_enddate Loans.EndDate%TYPE;
    v_remaining NUMBER;
    v_name varchar2(20);

    CURSOR Loan_cursor IS
        SELECT c.name,l.EndDate FROM Loans l 
        join Customers c ON c.CustomerID=l.CustomerID;

BEGIN
    OPEN Loan_cursor;

    LOOP
        FETCH Loan_cursor INTO v_name,v_enddate;
        EXIT WHEN Loan_cursor%NOTFOUND;

        v_remaining:=TRUNC(v_enddate-SYSDATE);

        IF v_remaining<=30 THEN
            dbms_output.put_line('Hurry up!'||v_name||' You have less than 30 days to pay the loan');
        ELSE
            dbms_output.put_line('HEY!'||v_name||' You have '||v_remaining ||' days to pay the loan');
        END IF;
    END LOOP;

    CLOSE Loan_cursor;
END;
