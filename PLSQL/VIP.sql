DECLARE
    c_id        Customers.CustomerID%TYPE;
    c_name      Customers.Name%TYPE;
    c_balance   Customers.Balance%TYPE;
    isVIP       BOOLEAN;

    CURSOR customer_cursor IS
        SELECT CustomerID, Name, Balance FROM Customers;

BEGIN
    OPEN customer_cursor;

    LOOP
        FETCH customer_cursor INTO c_id, c_name, c_balance;
        EXIT WHEN customer_cursor%NOTFOUND;

        IF c_balance > 10000 THEN
            isVIP := TRUE;
        ELSE
            isVIP := FALSE;
        END IF;

        IF isVIP = TRUE THEN
            DBMS_OUTPUT.PUT_LINE(c_name || ' (ID: ' || c_id || ') - Balance: ' || c_balance || ' --> VIP Customer');
        ELSE
            DBMS_OUTPUT.PUT_LINE(c_name || ' (ID: ' || c_id || ') - Balance: ' || c_balance || ' --> Not a VIP');
        END IF;

    END LOOP;

    CLOSE customer_cursor;
END;
